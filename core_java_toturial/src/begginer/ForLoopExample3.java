package begginer;

public class ForLoopExample3 {

	public static void main(String[] args) {

		
		String[] myArray = {"KAMAL","JAMAL","SALAM","KARIM","SAMIR","DAWOOD" };
		
		for(int a = 0; a< myArray.length; a++) {
			
			System.out.println((a) + " = " + myArray[a]);
		}
		
	}

}
