package begginer;

public class SwitchCaseExample1 {

	public static void main(String[] args) {

		String name = "Kamal";
		
		switch (name) {
		case "samim" :
			System.out.println("this is samim");
			break;
		case "Hamid" :
		System.out.println("this is Hamid");
			break;
		case "Kamal":
			System.out.println("this is " + name);
				break;
		default:
			System.out.println("the name is not on list");
			
		}
		
	 
		
		
	}

}
